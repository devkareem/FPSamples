using System;
namespace FPConcept
{
    public static class Opertions
    {
        public static double AddOne(double val)
        {
            return val + 1;
        }

        public static double Square(double val)
        {
            return Math.Pow(val,2);
        }
        public static double SubtractTen(double val)
        {
            return val - 10;
        }
    }
}